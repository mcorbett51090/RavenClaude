#!/usr/bin/env python3
"""
rebind_office.py — report-regeneration pipeline stage 3 for the OFFICE lane: the Word
(`.docx`) surgical output engine (the `rebind-office` skill).

The Office analogue of `skills/rebind-html/rebind_html.py`. Applies a Binding Manifest
(`knowledge/binding-manifest.schema.json`, `format: "office"`) to a COPY of a `.docx`
template ("surgeon, not a renderer" — `knowledge/core-architecture-spec.md` §1) and emits
the regenerated `.docx`. It works on a copy the whole way: read `word/document.xml` out of
the OPC/ZIP package with stdlib `zipfile`, edit those bytes via the SHARED OOXML resolver in
`scripts/rr_anchor.py` (this module NEVER re-implements anchor resolution), then re-zip —
every OPC part other than the ones a binding names is copied through byte-for-byte.

Per §4's node taxonomy:

    frozen       — NO mutation. After every other binding is applied, the engine re-resolves
                   the node in the template AND the output and proves its outer OOXML byte
                   span is identical (`_assert_frozen_unchanged`) — a defensive proof, not an
                   assumption. `frozen` is EARNED upstream (the manifest's job); rebind only
                   guarantees a frozen node did not move.
    surgical     — STRIP the old value at the anchor, THEN WRITE the new one — two separate
                   byte splices, in that order (zero-literal construction). At the instant
                   between them the node provably carries no old instance value, by
                   construction, not by a downstream check. The value lives in the run/cell's
                   `w:t` text; run properties (`w:rPr`) and every other child are preserved.
    regenerate   — Same strip-then-write zero-literal invariant. A prose/run node is rebuilt
                   from new data via stdlib `string.Template` (dotted-placeholder subclass;
                   `jinja2` is OPTIONAL acceleration for native `{{ }}`/`{% %}` control
                   syntax). A raster/`w:drawing` node is force-regenerated by REPLACING the
                   referenced `word/media/*` binary with a FRESH capture supplied in new-data
                   — never by transplanting the old pixels, per the spec's construction rule
                   ("a transplanted binary blob cannot be proven data-free").
    needs-review — Left completely untouched, but a visible OOXML marker run/paragraph
                   (carrying a machine-readable token) is inserted adjacent to it, and it is
                   logged. Never ships silently — guarantee #2 made mechanical for Office.

Bindings are applied in **reverse document order** (RT1-F10): each anchor is a stable node
identity re-resolved on the current bytes just before its edit, and processing from the end
of the document backward means an earlier edit's byte/index shifts can never invalidate a
not-yet-applied anchor. (Known constraint: a `needs-review` marker inserted as a run that
*precedes* a `frozen` run in the SAME paragraph would shift that frozen run's sibling index;
the upstream classifier does not produce that pairing, and it is out of scope here.)

Usage:
    python3 rebind_office.py --template t.docx --manifest m.json --new-data d.json --out o.docx
    python3 rebind_office.py --template t.docx --manifest m.json --new-data d.json --out o.docx --pretty

Exit codes:
    0 — success (output written; a JSON change-manifest printed to stdout)
    2 — usage / path-guard / manifest-schema / anchor-not-found / missing-data-query /
        missing-new-data-key / invalid-docx error (message on stderr + a best-effort JSON
        error object on stdout)

Design constraints (binding):
    - stdlib-first: argparse, base64, io, json, os, posixpath, re, string, sys, zipfile,
      xml.etree. NO pip installs. `python-docx` / `docxtpl` are detected for parity only
      (the byte-splice engine is the SOLE code path); `jinja2` is optional acceleration for
      regenerate templates that use native Jinja control syntax.
    - Runs unmodified on Python 3.9.6: `from __future__ import annotations`; no PEP 604
      `X | Y` unions in runtime expressions, no `match` statement.
    - Path-guarded (mirrors ../rebind-html/rebind_html.py / ../../scripts/seed_defects.py):
      every path is relative or an in-repo absolute path, no `..`, must resolve inside the
      repo root; `--out` may never equal `--template` (works on a COPY).
    - No network, no subprocess inside the engine.

Fixture: tests/fixtures/report-regeneration/sample-report.docx. Shared OOXML anchor
grammar + resolver: ../../scripts/rr_anchor.py. Manifest shape: ../../knowledge/
binding-manifest.schema.json. Full architecture: ../../knowledge/core-architecture-spec.md
§1/§2.1/§4.
"""
